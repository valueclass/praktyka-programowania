"""
Module that contains utility functions
"""


def add(a: int, b: int) -> int:
    """
    Perform addition on arguments and return the result.

    :param int a: first argument
    :param int b: second argument
    :return: a + b
    """
    return a + b


def subtract(a: int, b: int) -> int:
    """
    Perform subtraction on arguments and return the result.

    :param int a: first argument
    :param int b: second argument
    :return: a - b
    """
    return a - b


def multiply(a: int, b: int) -> int:
    """
    Perform multiplication on arguments and return the result.

    :param int a: first argument
    :param int b: second argument
    :return: a * b
    """
    return a * b


def divide(a: int, b: int) -> float:
    """
    Perform division on arguments and return the result.

    :param int a: first argument
    :param int b: second argument
    :return: a * b
    """
    return a / b
